<?php
declare(strict_types=1);

echo "Тестовый скрипт для отладки\n";
echo "=========================\n\n";

// Установите точку останова на следующей строке
$testVar = "Это тестовая переменная";
echo "Значение переменной: $testVar\n";

// Еще одна точка останова здесь
for ($i = 1; $i <= 5; $i++) {
    echo "Итерация $i\n";
    // Можно установить точку останова внутри цикла
    $squared = $i * $i;
    echo "  Квадрат числа $i = $squared\n";
}

echo "\nТест завершен.\n";
